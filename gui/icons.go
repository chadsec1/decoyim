func (i *icon) get() ([]byte, error) {
	var err error

	if i.decoded == nil {
		i.decoded, err = hex.DecodeString(i.encoded)
	}

	return i.decoded, err
}

func (i *icon) GetPixbuf() gdki.Pixbuf {
	var err error

	if i.cached == nil {
		i.cached, err = i.createPixBuf()
		if err != nil {
			panic(err)
		}
	}

	return i.cached
}

func (i *icon) hash() string {
	bytes, _ := i.get()
	res := sha256.Sum256(bytes)
	return hex.EncodeToString(res[:])
}

func (i *icon) createPixBufWithSize(width, height int) (gdki.Pixbuf, error) {
	var w sync.WaitGroup
	pl, err := g.gdk.PixbufLoaderNew()
	if err != nil {
		return nil, err
	}

	w.Add(1)
	_ = pl.Connect("area-prepared", func() {
		defer w.Done()
	})

	_ = pl.Connect("size-prepared", func() {
		pl.SetSize(width, height)
	})

	bytes, err := i.get()
	if err != nil {
		return nil, err
	}

	if _, err := pl.Write(bytes); err != nil {
		return nil, err
	}

	if err := pl.Close(); err != nil {
		return nil, err
	}

	w.Wait() // should not use the buffer until the event has happened
	return pl.GetPixbuf()
}

func (i *icon) createPixBuf() (gdki.Pixbuf, error) {
	return i.createPixBufWithSize(i.width, i.height)
}

func getActualRootFolder() string {
	wd, _ := os.Getwd()
	if strings.HasSuffix(wd, "/gui") {
		return filepath.Join(wd, "../")
	}
	return wd
}

func (i *icon) getPath() string {
	iconPath := filepath.Join(getActualRootFolder(), i.path)
	if fileNotFound(iconPath) {
		tmpIconPath := filepath.Join(filepath.Join(os.TempDir(), "decoyim"), i.name)
		if fileNotFound(tmpIconPath) {
			_ = os.MkdirAll(filepath.Join(os.TempDir(), "decoyim"), 0750)
			bytes, _ := i.get()
			_ = ioutil.WriteFile(tmpIconPath, bytes, 0600)
			log.WithFields(log.Fields{
				"name": i.name,
				"path": tmpIconPath,
			}).Debug("gui/icons: wrote file")
		}
		return tmpIconPath
	}
	return iconPath
}
